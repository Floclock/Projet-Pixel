    public function findByStyleQueryBuilder($style)
    {
        $qb = $this->createQueryBuilder('e')
        ->join('e.style', 's')
        ->addSelect('s')
        ->where('e.style = :myStyle')
        ->setParameter('myStyle', $style)
        ;
